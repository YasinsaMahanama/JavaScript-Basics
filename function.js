function add(fn,sn){
    return fn+sn;
}

console.log(add(10,8));


function remainder(fn,sn){
    return fn%sn;
}

console.log(remainder(10,4));


function substract(fn,sn){
    return fn-sn;
}

console.log(substract(10,4));


function division(fn,sn){
    return fn/sn;
}

console.log(division(10,4));


function multiplication(fn,sn){
    return fn*sn;
}

console.log(multiplication(10,4));


function exponentiation(fn,sn){
    return fn**sn;
}

console.log(exponentiation(10,4));


